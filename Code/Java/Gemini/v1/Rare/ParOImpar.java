package Code.Gemini.v1.Rare;

public class ParOImpar {
    public static void main(String[] args) {
        int numero = 27;

        if (numero % 2 == 0) {
            System.out.println(numero + " es un número par.");
        } else {
            System.out.println(numero + " es un número impar.");
        }
    }
}
