package dci.exp.one.abacus.v1.rare;

public class RestaNumeros {
    public static void main(String[] args) {
        double numero1 = 50.5;
        double numero2 = 20.3;
        double resta = numero1 - numero2;
        System.out.println("La resta de " + numero1 + " - " + numero2 + " = " + resta);
    }
}