package dci.exp.one.abacus.v1.rare;

public class PuntoMedio {
    public static void main(String[] args) {
        double x1 = 2, y1 = 3;
        double x2 = 8, y2 = 7;
        double xMedio = (x1 + x2) / 2;
        double yMedio = (y1 + y2) / 2;
        System.out.println("Punto medio: (" + xMedio + ", " + yMedio + ")");
    }
}